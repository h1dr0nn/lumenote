export const t = (key: keyof typeof translations.vi, lang: Language, params?: Record<string, any>) => {
  let text = translations[lang][key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
};
